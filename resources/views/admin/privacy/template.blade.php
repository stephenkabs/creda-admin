<h5>1. Introduction</h5>
<p>
This Privacy Policy explains how we collect, use, store, and protect personal
and financial information when you use the <strong>Creda Loan Management System</strong>.
Creda is designed for modern lenders, and we are committed to protecting your
privacy and handling your data responsibly.
</p>

<h5>2. Information We Collect</h5>
<p>
We may collect and process the following types of information:
</p>
<ul>
    <li>Personal identification details (name, NRC/ID number, email address, phone number, physical address)</li>
    <li>Client and borrower information (loan applications, guarantor details, employment or income information)</li>
    <li>Loan and financial data (loan amounts, repayment schedules, interest, penalties, transaction history)</li>
    <li>System usage data (login activity, audit logs, IP addresses, device information)</li>
</ul>

<h5>3. How We Use Your Information</h5>
<p>
Your information is used strictly for legitimate lending and operational purposes, including:
</p>
<ul>
    <li>Processing loan applications and approvals</li>
    <li>Managing clients, borrowers, and guarantors</li>
    <li>Tracking repayments, arrears, defaults, and penalties</li>
    <li>Generating financial reports, analytics, and operational insights</li>
    <li>Ensuring system security, auditability, and regulatory compliance</li>
</ul>

<h5>4. Data Storage &amp; Security</h5>
<p>
We implement appropriate technical and organizational measures to protect
personal and financial data against unauthorized access, loss, alteration, or
misuse. Access to sensitive information is restricted to authorized users based
on assigned roles and permissions.
</p>

<h5>5. Data Sharing</h5>
<p>
We do not sell, trade, or rent personal or financial data to third parties.
Data may only be shared where required by law, regulatory authorities, or with
trusted service providers strictly for system functionality and integration.
</p>

<h5>6. Data Retention</h5>
<p>
Personal and financial data is retained only for as long as necessary to fulfill
operational, legal, regulatory, and reporting obligations in accordance with
applicable lending and financial regulations.
</p>

<h5>7. User &amp; Organization Responsibilities</h5>
<p>
Organizations using the Creda platform are responsible for ensuring that client
and borrower data entered into the system is accurate, lawful, and collected
with proper consent where required by applicable laws and regulations.
</p>

<h5>8. Your Rights</h5>
<p>
Subject to applicable laws, clients and users may have the right to request
access to, correction of, or deletion of their personal information. Such requests
should be directed to the organization administering the Creda system.
</p>

<h5>9. Changes to This Policy</h5>
<p>
We may update this Privacy Policy from time to time. Any changes will be
published within the Creda platform, and continued use of the system constitutes
acceptance of the updated policy.
</p>

<h5>10. Contact Information</h5>
<p>
If you have questions or concerns about this Privacy Policy or how data is handled
within the Creda Loan Management System, please contact your system administrator
or the Creda platform provider.
</p>
